    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultListModel lista = (DefaultListModel) this.listArticulos.getModel();
        if (!this.txtcantidad.getText().isEmpty() && !lista.isEmpty()) {

            int cantidad = Integer.parseInt(this.txtcantidad.getText());

            elementosFacturas elementos = new elementosFacturas();
            elementos.agregarElementosFacturas(this.listArticulos, cantidad);
            this.tablearticulos.setModel(elementos.mostrarElementosFacturas(nrofactura));

        }

    }//GEN-LAST:event_jButton2ActionPerformed

