    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int filaSeleccionada = this.tablearticulos.getSelectedRow();

        String codigoa = this.tablearticulos.getValueAt(filaSeleccionada, 1).toString();
        int cantidad = Integer.parseInt(this.txtcantidad.getText());

        elementosFacturas elementos = new elementosFacturas();
        int escoger = JOptionPane.showConfirmDialog(null, "Desea realizar esta actualizacion de la factura?");
        if (escoger == 0) {
            elementos.actualizarElementoFactura(nrofactura, codigoa, cantidad);
            this.tablearticulos.setModel(elementos.mostrarElementosFacturas(nrofactura));
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int filaSeleccionada = this.tablearticulos.getSelectedRow();
        String codigoa = this.tablearticulos.getValueAt(filaSeleccionada, 1).toString();
         elementosFacturas elementos = new elementosFacturas();
        int escoger = JOptionPane.showConfirmDialog(null, "Desea realizar esta eliminacion de la factura?");
        if (escoger == 0) {
            elementos.eliminarElementoFactura(nrofactura, codigoa);
            this.tablearticulos.setModel(elementos.mostrarElementosFacturas(this.nrofactura));
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

