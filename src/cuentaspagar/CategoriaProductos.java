    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!this.txtcodigocat.getText().isEmpty() && !this.txtdescripcioncat.getText().isEmpty() && (!this.rbreg.isSelected() || !this.rbtBasico.isSelected())) {

            String codigo = this.txtcodigocat.getText();
            String descripcion = this.txtdescripcioncat.getText();
            char cat = 'A';
            if (this.rbreg.isSelected()) {
                cat = 'A';
            } else if (this.rbtBasico.isSelected()) {
                cat = 'B';
            }

            Categorias categoria = new Categorias(codigo, descripcion, cat);
            categoria.agregar();
            this.tablecata.setModel(categoria.mostrar());
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!this.txtcodigocat.getText().isEmpty() && !this.txtdescripcioncat.getText().isEmpty() && (!this.rbreg.isSelected() || !this.rbtBasico.isSelected())) {

            String codigo = this.txtcodigocat.getText();
            String descripcion = this.txtdescripcioncat.getText();
            char cat = 'A';
            if (this.rbreg.isSelected()) {
                cat = 'A';
            } else if (this.rbtBasico.isSelected()) {
                cat = 'B';
            }

            Categorias categoria = new Categorias(codigo, descripcion, cat);

            int decision = JOptionPane.showConfirmDialog(null, "Esta seguro que desea actualizar esta categoria?");

            if (decision == 0) {
                categoria.actualizar();
                this.tablecata.setModel(categoria.mostrar());
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

