    private void tablecataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablecataMouseClicked

        int filaSeleccionada = this.tablecata.rowAtPoint(evt.getPoint());
        this.txtcodigocat.setText(this.tablecata.getValueAt(filaSeleccionada, 0).toString());
        this.txtdescripcioncat.setText(this.tablecata.getValueAt(filaSeleccionada, 1).toString());
        if(this.tablecata.getValueAt(filaSeleccionada, 2).toString().charAt(0)=='A'){
            this.rbreg.setSelected(true);
        }else if(this.tablecata.getValueAt(filaSeleccionada, 2).toString().charAt(0)=='B'){
            this.rbtBasico.setSelected(true);
        }
        
        


    }//GEN-LAST:event_tablecataMouseClicked


