    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
   int filaSeleccionada = this.tableFacturas.getSelectedRow();
        if (!this.txtfechaini.getText().isEmpty() && !this.txtfechaven.getText().isEmpty() && (!this.rbdolares.isSelected() || !this.rbeuros.isSelected() || !this.rbotros.isSelected()) && this.cbproveedores.getSelectedIndex() > 0) {

            int nrofactura = Integer.parseInt(this.tableFacturas.getValueAt(filaSeleccionada, 0).toString());

            String proveedor = this.cbproveedores.getSelectedItem().toString();

            String fechaString1 = this.txtfechaini.getText();
            long fechaini = Date.parse(fechaString1);
            Date fechaDate1 = new Date(fechaini);

            String fechaString2 = this.txtfechaven.getText();
            long fechaven = Date.parse(fechaString2);
            Date fechaDate2 = new Date(fechaven);

            char formapago = 'D';
            if (this.rbdolares.isSelected()) {
                formapago = 'D';
            } else if (this.rbeuros.isSelected()) {
                formapago = 'E';
            } else if (this.rbotros.isSelected()) {
                formapago = 'O';
            }

            Facturas facturas = new Facturas(nrofactura, proveedor, fechaDate1, fechaDate2, formapago, 'A');

            int decision = JOptionPane.showConfirmDialog(null, "Esta seguro que desea Pagar esta factura?");

            if (decision == 0) {
                facturas.actualizarPagada();
                this.tableFacturas.setModel(facturas.mostrar());
            }

        }    
    }//GEN-LAST:event_jButton9ActionPerformed

