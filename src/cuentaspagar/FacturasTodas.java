    private void tableFacturasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableFacturasMouseClicked

        int filaSeleccionada = this.tableFacturas.rowAtPoint(evt.getPoint());
        this.txtfechaini.setText(this.tableFacturas.getValueAt(filaSeleccionada, 1).toString());
        this.txtfechaven.setText(this.tableFacturas.getValueAt(filaSeleccionada, 2).toString());
        if (this.tableFacturas.getValueAt(filaSeleccionada, 3).toString().charAt(0) == 'D') {
            this.rbdolares.setSelected(true);
        } else if (this.tableFacturas.getValueAt(filaSeleccionada, 3).toString().charAt(0) == 'E') {
            this.rbeuros.setSelected(true);
        } else if (this.tableFacturas.getValueAt(filaSeleccionada, 3).toString().charAt(0) == 'O') {
            this.rbotros.setSelected(true);
        }
        this.cbproveedores.setSelectedItem(this.tableFacturas.getValueAt(filaSeleccionada, 5).toString());


    }//GEN-LAST:event_tableFacturasMouseClicked

