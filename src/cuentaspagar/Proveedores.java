    private void tableProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProveedoresMouseClicked

        int filaSeleccionada = this.tableProveedores.rowAtPoint(evt.getPoint());
        this.txtrif.setText(this.tableProveedores.getValueAt(filaSeleccionada, 0).toString());
        this.txtrsocial.setText(this.tableProveedores.getValueAt(filaSeleccionada, 1).toString());
        this.txtdireccionp.setText(this.tableProveedores.getValueAt(filaSeleccionada, 2).toString());
        this.txttelefono.setText(this.tableProveedores.getValueAt(filaSeleccionada, 3).toString());

    }//GEN-LAST:event_tableProveedoresMouseClicked


